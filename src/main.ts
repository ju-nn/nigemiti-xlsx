function snapshot(current: State): StateSnapshot {
  return {
    beat: current.beat,
    flags: [...current.flags],
    choices: [...current.choices],
    inlineResult: current.inlineResult,
    sidePanel: current.sidePanel,
    finished: current.finished,
    log: current.log.map((entry) => ({ ...entry, lines: [...entry.lines] })),
    textSize: current.textSize,
    animations: current.animations,
    viewingLog: current.viewingLog,
  };
}

function inflateState(saved: Partial<StateSnapshot> | null): State {
  const base = initialState();
  if (!saved || typeof saved.beat !== 'number') return base;

  return {
    beat: Math.min(Math.max(saved.beat, 0), beats.length - 1),
    flags: Array.isArray(saved.flags) ? saved.flags.filter((flag): flag is string => typeof flag === 'string') : [],
    choices: Array.isArray(saved.choices) ? saved.choices.filter((choice): choice is string => typeof choice === 'string') : [],
    inlineResult: typeof saved.inlineResult === 'string' ? saved.inlineResult : '',
    sidePanel:
      saved.sidePanel === 'people' || saved.sidePanel === 'items' || saved.sidePanel === 'notes' || saved.sidePanel === 'log'
        ? saved.sidePanel
        : 'people',
    finished: Boolean(saved.finished),
    history: [],
    log: Array.isArray(saved.log)
      ? saved.log
          .filter((entry): entry is LogEntry => typeof entry?.beat === 'number' && typeof entry.title === 'string' && Array.isArray(entry.lines))
          .map((entry) => ({
            beat: Math.min(Math.max(entry.beat, 0), beats.length - 1),
            year: typeof entry.year === 'string' ? entry.year : '',
            title: entry.title,
            lines: entry.lines.filter((line): line is string => typeof line === 'string'),
          }))
      : [],
    textSize: saved.textSize === 'small' || saved.textSize === 'medium' || saved.textSize === 'large' ? saved.textSize : 'medium',
    animations: typeof saved.animations === 'boolean' ? saved.animations : true,
    viewingLog: Boolean(saved.viewingLog),
  };
}

function loadState(): State {
  try {
    return inflateState(JSON.parse(localStorage.getItem(SAVE_KEY) ?? 'null') as Partial<StateSnapshot> | null);
  } catch {
    return initialState();
  }
}

function saveState(): void {
  localStorage.setItem(SAVE_KEY, JSON.stringify(snapshot(state)));
}

function pushHistory(): void {
  state.history.push(snapshot(state));
  if (state.history.length > 80) state.history.shift();
}

function goBack(): void {
  const previous = state.history.pop();
  if (!previous) return;
  const history = [...state.history];
  state = { ...inflateState(previous), history };
  state.viewingLog = false;
  render();
}

function restartFromEnding(): void {
  state = initialState();
  addCurrentLogEntry();
  localStorage.removeItem(SAVE_KEY);
  document.title = '逃げ道.xlsx';
  render();
}

function getXPostUrl(): string {
  const params = new URLSearchParams({
    text: '無料ノベルゲーム『逃げ道.xlsx』をクリアしました。\n\n会社員が「月曜の朝」から逃げ道を探す、短編分岐ノベルゲーム。',
    url: SHARE_URL,
    hashtags: '逃げ道xlsx',
  });
  return `https://twitter.com/intent/tweet?${params.toString()}`;
}

function makeLogEntry(current: State): LogEntry {
  const beat = beats[current.beat];
  const lines = current.inlineResult ? [`選択: ${current.inlineResult}`, ...beat.body(current)] : beat.body(current);
  return {
    beat: current.beat,
    year: beat.year,
    title: beat.title,
    lines,
  };
}

function addCurrentLogEntry(): void {
  if (state.finished) return;
  const current = makeLogEntry(state);
  const last = state.log.at(-1);
  if (last?.beat === current.beat && last.title === current.title) {
    state.log[state.log.length - 1] = current;
  } else {
    state.log.push(current);
  }
}

function render(): void {
  const beat = beats[state.beat];

  nodes.tabs.forEach((button) => {
    button.classList.toggle('is-active', button.dataset.panel === state.sidePanel);
  });
  if (nodes.back) nodes.back.disabled = state.history.length === 0;
  if (nodes.logToggle) nodes.logToggle.classList.toggle('is-active', state.viewingLog);
  document.documentElement.dataset.textSize = state.textSize;
  document.documentElement.dataset.animations = state.animations ? 'on' : 'off';

  if (nodes.cast) {
    const side = renderSidePanel(state);
    if (nodes.sideTitle) nodes.sideTitle.textContent = side.title;
    nodes.cast.innerHTML = side.html;
    animateUpdatedNotes();
    animatePanelItems();
  }

  nodes.layout?.setAttribute('data-scene', getSceneKey(state));
  if (nodes.sceneCaption) nodes.sceneCaption.textContent = getSceneCaption(state);

  const pathSummary = document.querySelector<HTMLDivElement>('#path-summary');
  if (pathSummary) pathSummary.innerHTML = renderPathSummary(state);

  if (state.finished) {
    const changedTitle = '生活.xlsx';
    document.title = changedTitle;
    if (nodes.appTitle && nodes.appTitle.textContent !== changedTitle) {
      nodes.appTitle.textContent = changedTitle;
      animateScramble(nodes.appTitle, changedTitle, 'title');
    }
    if (nodes.year) nodes.year.textContent = '結果';
    if (nodes.progress) nodes.progress.textContent = 'END';
    if (nodes.progressBar) nodes.progressBar.style.width = '100%';
    if (nodes.title) nodes.title.textContent = '佐久間の生活は、どこかへ着いた。';
    if (nodes.body) nodes.body.innerHTML = `<p class="ending-card">${getEnding()}</p>`;
    if (nodes.choices) {
      const xPostUrl = escapeHtml(getXPostUrl());
      const creatorUrl = escapeHtml(CREATOR_SITE_URL);
      nodes.choices.innerHTML = `
        <div class="ending-actions">
          <button class="continue" type="button" data-ending-action="restart">
            <span>最初から</span>
            <small>逃げ道.xlsx を開き直す</small>
          </button>
          <a class="creator-link" href="${creatorUrl}" target="_blank" rel="noopener noreferrer">
            <span>作者サイトへ</span>
            <small>作品、note、SNS、ラジオ、vlogへ</small>
          </a>
          <a class="x-post" href="${xPostUrl}" target="_blank" rel="noopener noreferrer">
            <span>Xで感想を投稿</span>
            <small>ネタバレなしの定型文で開く</small>
          </a>
        </div>
      `;
      nodes.choices.querySelector<HTMLButtonElement>('[data-ending-action="restart"]')?.addEventListener('click', restartFromEnding);
      animateChoices();
    }
    saveState();
    return;
  }

  if (state.viewingLog) {
    document.title = '読書ログ - 逃げ道.xlsx';
    if (nodes.year) nodes.year.textContent = 'LOG';
    if (nodes.progress) nodes.progress.textContent = '読書ログ';
    if (nodes.progressBar) nodes.progressBar.style.width = `${Math.max(3, ((state.beat + 1) / beats.length) * 100)}%`;
    if (nodes.title) nodes.title.textContent = '読書ログ';
    if (nodes.body) {
      nodes.body.innerHTML = renderMainLog(state);
      nodes.storyScroll?.scrollTo({ top: 0 });
      requestAnimationFrame(updateReaderState);
    }
    if (nodes.choices) {
      nodes.choices.innerHTML = '<button class="continue" type="button"><span>本文に戻る</span><small>現在の場面へ</small></button>';
      nodes.choices.querySelector<HTMLButtonElement>('button')?.addEventListener('click', () => {
        state.viewingLog = false;
        saveState();
        render();
      });
      animateChoices();
    }
    saveState();
    return;
  }

  document.title = '逃げ道.xlsx';
  if (nodes.appTitle && nodes.appTitle.textContent !== '逃げ道.xlsx') {
    nodes.appTitle.textContent = '逃げ道.xlsx';
  }

  if (nodes.year) nodes.year.textContent = beat.year;
  if (nodes.progress) nodes.progress.textContent = getPlaceLabel(beat.title);
  if (nodes.progressBar) nodes.progressBar.style.width = `${Math.max(3, ((state.beat + 1) / beats.length) * 100)}%`;
  if (nodes.title) {
    nodes.title.textContent = beat.title;
    animateScramble(nodes.title, beat.title, 'title');
  }
  if (nodes.body) {
    const body = state.inlineResult ? [`選択: ${state.inlineResult}`, ...beat.body(state)] : beat.body(state);
    nodes.body.innerHTML = body
      .map((line, index) => `<p class="${state.inlineResult && index === 0 ? 'result-line' : ''}">${decorateClues(line)}</p>`)
      .join('');
    nodes.storyScroll?.scrollTo({ top: 0 });
    requestAnimationFrame(updateReaderState);
  }

  if (!nodes.choices) return;

  if (beat.choices) {
    nodes.choices.innerHTML = beat.choices
      .map(
        (choice, index) => `
          <button type="button" data-choice="${index}">
            <span>${choice.label}</span>
            <small>${choice.note}</small>
          </button>
        `,
      )
      .join('');

    nodes.choices.querySelectorAll<HTMLButtonElement>('button').forEach((button) => {
      button.addEventListener('click', () => {
        const choice = beat.choices?.[Number(button.dataset.choice)];
        if (choice) applyChoice(choice);
      });
    });
    animateChoices();
  } else {
    nodes.choices.innerHTML = '<button class="continue" type="button"><span>読み進める</span><small>次へ</small></button>';
    nodes.choices.querySelector<HTMLButtonElement>('button')?.addEventListener('click', next);
    animateChoices();
  }
  saveState();
}

function animateScramble(target: HTMLElement, text: string, mode: 'title' | 'note'): void {
  if (prefersReducedMotion()) {
    target.textContent = text;
    return;
  }

  animeAnimate(target, {
    innerHTML: scrambleText({
      text,
      chars: mode === 'title' ? 'shades' : 'numbers',
      cursor: mode === 'title' ? '░▒▓█' : '_',
      revealRate: mode === 'title' ? 72 : 95,
      settleDuration: mode === 'title' ? 220 : 150,
      perturbation: 0.65,
    }),
    duration: mode === 'title' ? 720 : 460,
    ease: 'out(3)',
  });
}

function animateChoices(): void {
  if (!nodes.choices || prefersReducedMotion()) return;

  motionAnimate(
    Array.from(nodes.choices.querySelectorAll('button, a')),
    { opacity: [0, 1], y: [8, 0] },
    { duration: 0.24, delay: 0.04, ease: 'easeOut' },
  );
}

function animatePanelItems(): void {
  if (!nodes.cast || prefersReducedMotion()) return;

  motionAnimate(
    Array.from(nodes.cast.querySelectorAll('p')),
    { opacity: [0.72, 1], x: [4, 0] },
    { duration: 0.22, delay: 0.02, ease: 'easeOut' },
  );
}

function animateUpdatedNotes(): void {
  if (!nodes.cast || state.sidePanel !== 'people' || state.viewingLog) return;

  nodes.cast.querySelectorAll<HTMLElement>('.is-updated span').forEach((node) => {
    const text = node.textContent ?? '';
    animateScramble(node, text, 'note');
  });
}

function escapeHtml(text: string): string {
  return text
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#039;');
}

function decorateClues(line: string): string {
  const clues = [
    '真島_未送信.eml',
    '本人都合',
    '除籍',
    '二十二時十四分',
    '業務配分を見直す',
    '異変に気づいたら声をかけよう',
    '逃げ道.xlsx',
  ];

  let html = escapeHtml(line);
  clues.forEach((clue) => {
    const escaped = escapeHtml(clue);
    html = html.replace(escaped, `<span class="clue-mark">${escaped}</span>`);
  });
  return html;
}

