function applyChoice(choice: Choice): void {
  pushHistory();
  state.viewingLog = false;
  state.flags.push(choice.tag);
  state.choices.push(choice.label);
  state.inlineResult = choice.result;
  state.beat += 1;
  addCurrentLogEntry();
  render();
}

function next(): void {
  pushHistory();
  state.viewingLog = false;
  if (state.beat >= beats.length - 1) {
    state.finished = true;
  } else {
    state.beat += 1;
    addCurrentLogEntry();
  }
  state.inlineResult = '';
  render();
}

