function getSceneKey(current: State): string {
  const title = current.finished ? '' : beats[current.beat]?.title ?? '';

  if (current.finished || hasReached(current, '平日') || hasReached(current, 'セミリタイア')) {
    return 'weekday';
  }

  if (title.includes('カレー') || title.includes('伝票') || title.includes('正解')) return 'curry';
  if (title.includes('Timee')) return 'warehouse';
  if (title.includes('Uber Eats')) return 'delivery';
  if (title.includes('梨') || title.includes('親に説明')) return 'family';
  if (title.includes('昇進') || title.includes('高瀬課長') || title.includes('人事部')) return 'meeting';
  if (title.includes('証券') || title.includes('暴落')) return 'restroom';
  if (title.includes('退職金') || title.includes('辞める') || title.includes('朝は勝手')) return 'decision';
  if (
    title.includes('レシート') ||
    title.includes('四パーセント') ||
    title.includes('ミニマリスト') ||
    title.includes('副業') ||
    title.includes('動画') ||
    title.includes('ベーシック') ||
    title.includes('冷蔵庫') ||
    title.includes('欠勤')
  ) {
    return 'apartment';
  }

  return 'office';
}

function getSceneCaption(current: State): string {
  const labels: Record<string, string> = {
    apartment: '自室の夜',
    curry: '駅前のカレー屋',
    decision: '申請書の机',
    delivery: '配達の坂道',
    family: '実家の食卓',
    meeting: '面談室',
    office: '深夜の経理部',
    restroom: '会社の個室',
    warehouse: '郊外の倉庫',
    weekday: '平日の生活圏',
  };

  return labels[getSceneKey(current)] ?? labels.office;
}

function updateReaderState(): void {
  if (!nodes.storyReader || !nodes.storyScroll) return;

  const { scrollTop, scrollHeight, clientHeight } = nodes.storyScroll;
  const overflow = scrollHeight > clientHeight + 2;
  nodes.storyReader.classList.toggle('has-overflow', overflow);
  nodes.storyReader.classList.toggle('is-at-top', scrollTop <= 2);
  nodes.storyReader.classList.toggle('is-at-bottom', scrollTop + clientHeight >= scrollHeight - 2);
}

function renderSidePanel(current: State): { title: string; html: string } {
  if (current.sidePanel === 'items') {
    return { title: '持ち物', html: `${renderItems(current)}${renderSettings(current)}` };
  }

  if (current.sidePanel === 'notes') {
    return { title: 'メモ', html: `${renderNotes(current)}${renderSettings(current)}` };
  }

  return { title: '人物メモ', html: `${renderCastNotes(current)}${renderSettings(current)}` };
}

